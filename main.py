# Taking input form user
num_1 = int(input("Enter a number: "))
num_2 = int(input("Enter another number: "))

# Giving output to user
print(f"{num_1} + {num_2} = {num_1 + num_2}")
print(f"{num_1} - {num_2} = {num_1 - num_2}")
print(f"{num_1} * {num_2} = {num_1 * num_2}")
print(f"{num_1} / {num_2} = {num_1 / num_2}")
